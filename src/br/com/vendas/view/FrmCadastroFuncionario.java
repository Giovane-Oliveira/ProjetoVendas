    private void BtnFuncionarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnFuncionarioActionPerformed
        // TODO add your handling code here:
        FuncionariosDAO dao = new FuncionariosDAO();
        Funcionarios obj = dao.consultarFuncionariosPorNome(NomeFuncionario.getText());
        
        if(obj.getNome() != null){
        
        CodigoFuncionario.setText(String.valueOf(obj.getId()));
        NomeFuncionario.setText(obj.getNome());
        RgFuncionario.setText(obj.getRg());
       CpfFuncionario.setText(obj.getCpf());
       EmailFuncionario.setText(obj.getEmail());
       SenhaFuncionario.setText(obj.getEmail());
       CargoFuncionario.setText(obj.getEmail());
       tipoUsuarioFuncionario.setSelectedItem(obj.getEmail());
       TelefoneFuncionario.setText(obj.getTelefone());
       CelularFuncionario.setText(obj.getCelular());
       CepFuncionario.setText(obj.getCep());
       EnderecoFuncionario.setText(obj.getEndereco());
       NumeroFuncionario.setText(String.valueOf(obj.getNumero()));
       ComplementoFuncionario.setText(obj.getComplemento());
       BairroFuncionario.setText(obj.getBairro());
       CidadeFuncionario.setText(obj.getBairro());
       EstadoFuncionario.setSelectedItem(obj.getEstado());
        
        }else{
        JOptionPane.showMessageDialog(null, "Usuário não encontrado");
        
        }
        
        
    }//GEN-LAST:event_BtnFuncionarioActionPerformed

