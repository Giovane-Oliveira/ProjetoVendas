   public void listar(){
   
   FuncionariosDAO dao =  new FuncionariosDAO();
   List<Funcionarios> lista = dao.listarFuncionarios();
   DefaultTableModel dados = (DefaultTableModel) TabelaFuncionarios.getModel();
   dados.setNumRows(0);
   for(Funcionarios c : lista){
   
   dados.addRow(new Object[]{
   c.getId(),
   c.getNome(),
   c.getRg(),
   c.getCpf(),
   c.getEmail(),
   c.getSenha(),
   c.getCargo(),
   c.getTipoUsuario(),
   c.getTelefone(),
   c.getCelular(),
   c.getCep(),
   c.getEndereco(),
   c.getNumero(),
   c.getComplemento(),
   c.getBairro(),
   c.getCidade(),
   c.getEstado()
   
           
   
   });
   }
   
   
   }

